def aumentar(num):

    while True:
        valor = num * (1 + 10/100)
        break
    return valor

def diminuir(num):

    while True:
        reajuste = num * (1 - 13/100)
        break
    return reajuste

def dobro(num):

    while True:
        dobro = num * 2
        break
    return dobro

def metade(num):

    while True:
        
        metade = num / 2
        break
    return metade
